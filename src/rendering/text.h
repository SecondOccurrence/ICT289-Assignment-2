#ifndef TEXT_H
#define TEXT_H

/**
 * @brief displays text on the screen indicating cue hit strength
 *
 * @return void
 * @post cue hit strength as a percentage is displayed in the top left corner of screen
 */
void text_power();

/**
 * @brief displays text on screen indicating remaining object balls left
 *
 * @return void
 * @post ball left is shown on top left of the screen
 */
void text_remaining();

#endif // TEXT_H
